# ------------------------------------------------------------ filter graphs
#
# The graph builders are pure functions — the loudnorm finish, the mix
# levels and the xfade chaining are asserted on the constructed graphs.


def test_export_audio_graph_finishes_with_loudnorm():
    graph = preview._export_audio_graph(
        "music", "1:a", None, [], 0.0, 0.0, 6.0
    )
    assert "loudnorm=I=-14:TP=-1:LRA=11" in graph
    assert graph.endswith("[a]")
    # loudnorm upsamples internally; the chain resamples back after it
    assert graph.index("loudnorm") < graph.index("aresample=48000")


def test_export_audio_graph_mix_levels_and_sfx():
    graph = preview._export_audio_graph(
        "mix", "4:a", "5:a", [("6:a", 2.0, 0.5)], 0.2, 0.5, 6.0
    )
    assert "volume=1" in graph and "volume=0.6" in graph  # the fixed mix
    assert "atrim=0:0.500" in graph  # trimmed to min(cue, file)
    assert "adelay=2000|2000" in graph  # placed at cue.time
    assert "normalize=0" in graph
    assert "afade=t=in:st=0:d=0.200" in graph
    assert "afade=t=out:st=5.500:d=0.500" in graph
    # the fades run BEFORE the loudness finish
    assert graph.index("afade") < graph.index("loudnorm")


def test_export_video_graph_chains_xfade_and_concat():
    # segment 1 dissolves in (0.4s), segment 2 is a hard cut; segment 0
    # was rendered 0.4s long to feed the overlap.
    graph = preview._export_video_graph(
        [2.4, 2.0, 2.0], [0.0, 0.4, 0.0], 0.2, 0.5, 6.0
    )
    # the xfade offset is the incoming segment's record position: the
    # timeline timing never moves (2.4 rendered - 0.4 overlap = 2.0)
    assert "xfade=transition=fade:duration=0.400:offset=2.000" in graph
    assert "concat=n=2:v=1:a=0" in graph
    assert "fade=t=in:st=0:d=0.200" in graph
    assert "fade=t=out:st=5.500:d=0.500" in graph
    assert graph.endswith("[v]")


def test_export_video_graph_hard_cuts_only():
    graph = preview._export_video_graph([2.0, 2.0], [0.0, 0.0], 0.0, 0.0, 4.0)
    assert "xfade" not in graph
    assert "concat=n=2:v=1:a=0" in graph



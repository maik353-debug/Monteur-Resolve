# ------------------------------------------------------------ robustness


@needs_ffmpeg
@needs_demo
def test_failed_render_leaves_no_temp_dir(tmp_path, monkeypatch):
    """A mid-render ffmpeg failure must still remove the segment dir."""
    workdir = tmp_path / "work"
    workdir.mkdir()
    real_mkdtemp = tempfile.mkdtemp
    monkeypatch.setattr(
        preview.tempfile,
        "mkdtemp",
        lambda prefix="", **kw: real_mkdtemp(prefix=prefix, dir=str(workdir)),
    )
    plan = demo_plan()
    plan.entries[1].clip_path = str(tmp_path / "missing.mp4")  # fails mid-render
    with pytest.raises(MonteurMediaError):
        render_preview(plan, str(tmp_path / "broken.mp4"))
    assert list(workdir.iterdir()) == []  # no leftover monteur-preview-* dirs


@needs_ffmpeg
@needs_demo
def test_successful_render_leaves_no_temp_dir(tmp_path, monkeypatch):
    workdir = tmp_path / "work"
    workdir.mkdir()
    real_mkdtemp = tempfile.mkdtemp
    monkeypatch.setattr(
        preview.tempfile,
        "mkdtemp",
        lambda prefix="", **kw: real_mkdtemp(prefix=prefix, dir=str(workdir)),
    )
    render_preview(demo_plan(), str(tmp_path / "ok.mp4"))
    assert list(workdir.iterdir()) == []

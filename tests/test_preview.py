@needs_ffmpeg
@needs_demo
def test_progress_callback_counts_to_total(tmp_path):
    calls: list[tuple[int, int, str]] = []
    render_preview(
        demo_plan(),
        str(tmp_path / "p.mp4"),
        progress=lambda done, total, label: calls.append((done, total, label)),
    )
    assert calls, "progress was never called"
    totals = {total for _, total, _ in calls}
    assert totals == {5}  # 4 segments + the final mux
    assert [done for done, _, _ in calls] == list(range(1, 6))
    assert calls[-1][0] == calls[-1][1]



# ============================================================ render_export
#
# The Direct Export engine. Tests render at a tiny explicit `size` (the
# documented advanced/testing override) — canvas presets are delivery
# resolutions and far too big for a test suite.


def _tiny_wav(path: Path, seconds: float = 0.5) -> str:
    """A short decaying 440 Hz ping — a stand-in for a placed SFX file."""
    rate = 44100
    n = int(seconds * rate)
    with wave.open(str(path), "wb") as fh:
        fh.setnchannels(1)
        fh.setsampwidth(2)
        fh.setframerate(rate)
        fh.writeframes(
            b"".join(
                struct.pack(
                    "<h",
                    int(
                        20000
                        * math.sin(2 * math.pi * 440 * i / rate)
                        * math.exp(-i / (rate * 0.2))
                    ),
                )
                for i in range(n)
            )
        )
    return str(path)


def _titles_expected_to_draw() -> bool:
    """Whether THIS environment can draw act titles (filter + font)."""
    return preview._supports_drawtext() and preview._find_font() is not None


